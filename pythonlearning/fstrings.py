# Formatting numbers
num = 3.14159
formatted = f"The value of pi is {num:.2f}"
print(formatted)  # Output: "The value of pi is 3.14"

# Calling methods
name = "Alice"
formatted = f"Hello, {name.upper()}"
print(formatted)  # Output: "Hello, ALICE"

# Using dictionary keys
person = {'name': 'Bob', 'age': 25}
formatted = f"{person['name']} is {person['age']} years old."
print(formatted)  # Output: "Bob is 25 years old."
